# fazer a conta do aumento do funcionario: preciso do salario atual e da porcentagem de aumento. 
# mostrar na tela o novo salario e o valor do aumento.
salario_atual = float(input("Digite o salário atual do funcionário: "))
porcentagem_aumento = float(input("Digite a porcentagem de aumento: "))
valor_aumento = salario_atual * (porcentagem_aumento / 100)
novo_salario = salario_atual + valor_aumento
print("O valor do aumento é:", valor_aumento)
print("O novo salário do funcionário é:", novo_salario)

