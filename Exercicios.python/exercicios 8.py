# fazer o calculo de media gasto pelo usuario: o usuario vai digitar a quantidade de km rodados e a quantidade de combustivel consumida.
quilometros_rodados = float(input("Digite a quantidade de quilômetros rodados: "))
combustivel_consumido = float(input("Digite a quantidade de combustível consumido em litros: "))
media_consumo = quilometros_rodados / combustivel_consumido
print("A média de consumo de combustível é:", media_consumo, "km/l")

